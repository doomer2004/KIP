import math
x = float(input('x:')) #введення координати х
y = float(input('y:')) #введення координати y
if math.fabs(x) > 1 and math.fabs(y) > 1:
    print('Точка не належить заштрихованій частині площини'); #значення не підходять адже перевищують 1
elif y ** 2 <= math.fabs(x) and math.fabs(y) >= x ** 2:
    print('Точка  належить заштрихованій частині площини'); #значення підходять
else:
    print('Точка не належить заштрихованій частині площини'); #значення не підходять
